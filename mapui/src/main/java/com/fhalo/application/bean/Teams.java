package com.fhalo.application.bean;

public class Teams {

	private String teamName;
	private String teamAddress;
	private String teamLeftTime;
	public String getTeamName() {
		return teamName;
	}
	public void setTeamName(String teamName) {
		this.teamName = teamName;
	}
	public String getTeamAddress() {
		return teamAddress;
	}
	public void setTeamAddress(String teamAddress) {
		this.teamAddress = teamAddress;
	}
	public String getTeamLeftTime() {
		return teamLeftTime;
	}
	public void setTeamLeftTime(String teamLeftTime) {
		this.teamLeftTime = teamLeftTime;
	}
}
